<p>2. Sukurti puslapį panašų į 1 uždavinį, tiktai antro linko su perduodamu kintamuoju nedarykite, o vietoj to padarykite, URL eilutėje ranka įvedus GET kintamąjį color su RGB spalvos kodu (pvz color=ff1234) puslapio fono spalva pasidarytų tokios spalvos.</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/2.php">Solution 2</a></p>

<p>3. Perdarykite 2 uždavinį taip, kad spalvą būtų galimą įrašyti į laukelį ir ją išsiųsti mygtuku GET metodu formoje.</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/3.php">Solution 3</a></p>

<p>4. Sukurkite du puslapius lemon.php ir orange.php. Jų fonus nuspalvinkite atitinkamom spalvom. Į lemon.php puslapį įdėkite kodą, kuris naršyklę visada peradresuotų į puslapį orange.php. Pademonstruokite veikimą.</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/4/lemon.php">Lemon</a><br/><a href="http://localhost/bit/nd/webmech/4/orange.php">Orange</a></p>

<p>5. Sukurkite du atskirus puslapius blue.php ir red.php Juose sukurkite linkus į pačius save (abu į save ne į kitą puslapį!). Padarykite taip, kad paspaudus ant  linko puslapis ne tiesiog persikrautų, o PHP kodas (ne tiesiogiai html tagas!) naršyklę peradresuotų į kitą puslapį (iš raudono į mėlyną ir atvirkščiai).</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/5/blue.php">Blue</a><br/>
<a href="http://localhost/bit/nd/webmech/5/red.php">Red</a></p>

<p>6. Padarykite puslapį su dviem mygtukais. Mygtukus įdėkite į dvi skairtingas formas- vieną GET ir kitą POST. Nenaudodami jokių konkrečių $_GET ar $_POST reikšmių, nuspalvinkite foną žaliai, kai paspaustas mygtukas iš GET formos ir geltonai- kai iš POST.</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/6.php">Solution 6</a></p>

<p>7. Pakartokite 6 uždavinį. Papildykite jį kodu, kuris naršyklę po POST metodo peradresuotų tuo pačiu adresu (t.y. į patį save) jau GET metodu.</p>
<p class="solution"><a href="http://localhost/bit/nd/webmech/7.php">Solution 7</a></p>

<p>8. Sukurkite du puslapius pink.php ir rose.php. Nuspalvinkite juos atitinkamom spalvom. Į pink.php puslapį įdėkite formą su POST metodu ir mygtuku “GO TO ROSE”. Formą nukreipkite, kad ji atsidarinėtų rose.php puslapyje. Padarykite taip, kad surinkus naršyklėje tiesiogiai adresą į rose.php puslapį, naršyklė būtų peradresuojama į pink.php puslapį.</p>
